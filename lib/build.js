var path = require('path');
var rollup = require('rollup');
var rollupVue = require('rollup-plugin-vue');
var rollupJson = require('rollup-plugin-json');

var fileHelpers = require('./fileHelpers');

function build({ data, app, location }) {
  return Promise.all([
    generateJS({ data, app, location }),
    generateIndex({ title: data.title, location }),
  ]);
}

function generateJS({ data, app, location }) {
  return rollup.rollup({
    entry: app,
    plugins: [
      rollupVue({
        css: `${location}/novella.css`,
        compileTemplate: false,
      }),
      rollupJson(),
    ],
  }).then((bundle) => {
    bundle.write({
      format: 'iife',
      moduleName: 'Novella',
      indent: '  ',
      dest: `${location}/novella.js`,
      intro: `
console.log("Generated by novella.");
var data = ${JSON.stringify(data)};\n`,
      globals: {vue: 'Vue'},
    });
  });
}

function generateIndex({ title, location }) {
  var templateLocation = path.resolve(__dirname, './app/index.html');
  var indexLocation = path.resolve(location, 'index.html');

  return fileHelpers.read(templateLocation)
  .then((file) => {
    var indexHTML = file.contents.replace('%TITLE%', title);
    return fileHelpers.write(indexLocation, indexHTML);
  });
}

module.exports = build;
